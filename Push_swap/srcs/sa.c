#include "push_swap.h"

int	sa(t_stack *a, char *print)
{
	int	tmp;

	tmp = a->stack[0];
	a->stack[0] = a->stack[1];
	a->stack[1] = tmp;
	print_step(a, print);
	return (1);
}
