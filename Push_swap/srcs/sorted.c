#include "push_swap.h"

int 	sorted(t_stack *a)
{
	int	i;

	i = -1;
	while (++i < a->count - 1)
		if (a->stack[i] > a->stack[i + 1])
			break ;
	if (i == a->count - 1)
		return (1);
	return (0);
}
