#include "push_swap.h"

int	b_min(t_stack *b)
{
	int	i;
	int	b_min1;

	i = 0;
	b_min1 = b->stack[0];
	while (++i < b->count)
		if (b->stack[i] < b_min1)
			b_min1 = b->stack[i];
	return (b_min1);
}
