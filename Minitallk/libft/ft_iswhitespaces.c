_Bool		ft_iswhitespaces(int c)
{
	char	i;

	i = (unsigned char)c;
	if (i == ' ' || i == '\n' ||
		i == '\t' || i == '\v' ||
		i == '\f' || i == '\r')
		return (1);
	return (0);
}
