#include "libft.h"

_Bool		ft_isnumber(char *num)
{
	int		i;

	if (!num)
		return (0);
	if (*num == '-' || *num == '+')
		++num;
	i = 0;
	while (num[i] && ft_isdigit(num[i]))
		++i;
	return (num[i] ? 0 : 1);
}
